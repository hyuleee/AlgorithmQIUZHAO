//DFS

class Solution {

    private int n;
    private int m;

    public int numIslands(char[][] grid) {
        n = grid.length;
        if (n == 0) return 0;
        int count = 0;
        m = grid[0].length;
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < m; j++) {
                if (grid[i][j] == '1') {
                    dfsMaking(grid, i, j);
                    count++;
                }
            }
        }
        return count;
    }

    private void dfsMaking(char[][] grid, int i, int j) {
        if (i < 0 || j < 0 || i >= n || j >= m || grid[i][j] != '1') return;
        grid[i][j] = '0';
        dfsMaking(grid, i+1, j);
        dfsMaking(grid, i-1, j);
        dfsMaking(grid, i, j+1);
        dfsMaking(grid, i, j-1);
    }
}
